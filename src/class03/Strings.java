package class03;

public class Strings {
    public static void main (String[] args ) {
        int myInt = 7;

        String text = "Hello";

        String blank = " ";

        String name = "Pedro";

        String greeting = text + blank + name;

        System.out.println(greeting);

        System.out.println("Hello" + " " + "Pedro");

        System.out.println("My integer is: " + myInt);
    }    
}
